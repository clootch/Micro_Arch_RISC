module Instruction_Decoder(
    input clk,
	input [6:0] opcode,
	output reg [14:0] CW
);
    always @(posedge clk) begin
        case(opcode)
            //                RW MD BS PS MW FS    MB MA CS
            7'b0000000: CW = 15'b0__00_00_0__0__00000_0__0__0; //NOP            
            7'b0000010: CW = 15'b1__00_00_0__0__00010_0__0__0; //ADD
            7'b0000101: CW = 15'b1__00_00_0__0__00101_0__0__0; //SUB
            7'b1100101: CW = 15'b1__10_00_0__0__00101_0__0__0; //SLT      
            7'b0001000: CW = 15'b1__00_00_0__0__01000_0__0__0; //AND      
            7'b0001010: CW = 15'b1__00_00_0__0__01010_0__0__0; //OR      
            7'b0001100: CW = 15'b1__00_00_0__0__01100_0__0__0; //XOR       
            7'b0000001: CW = 15'b0__00_00_0__1__00000_0__0__0; //ST      
            7'b0100001: CW = 15'b1__01_00_0__0__00000_0__0__0; //LD       
            7'b0100010: CW = 15'b1__00_00_0__0__00010_0__0__0; //ADI       
            7'b0100101: CW = 15'b1__00_00_0__0__00101_0__0__0; //SBI       
            7'b0101110: CW = 15'b1__00_00_0__0__01110_0__0__0; //NOT      
            7'b0101000: CW = 15'b1__00_00_0__0__01000_0__0__0; //ANI      
            7'b0101010: CW = 15'b1__00_00_0__0__01010_0__0__0; //ORI      
            7'b0101100: CW = 15'b1__00_00_0__0__01100_0__0__0; //XRI      
            7'b1100010: CW = 15'b1__00_00_0__0__00010_0__0__0; //AIU      
            7'b1000101: CW = 15'b1__00_00_0__0__00101_0__0__0; //SIU      
            7'b1000000: CW = 15'b1__00_00_0__0__00000_0__0__0; //MOV      
            7'b0110000: CW = 15'b1__00_00_0__0__10000_0__0__0; //LSL      
            7'b0110001: CW = 15'b1__00_00_0__0__10001_0__0__0; //LSR      
            7'b1100001: CW = 15'b0__00_10_0__0__00000_0__0__0; //JMR      
            7'b0100000: CW = 15'b0__00_01_0__0__00000_0__0__0; //BZ  
            7'b1100000: CW = 15'b0__00_01_1__0__00000_0__0__0; //BNZ    
            7'b1000100: CW = 15'b0__00_11_0__0__00000_0__0__0; //JMP   
            7'b0000111: CW = 15'b1__00_11_0__0__00111_0__0__0; //JML
            default: CW = 15'b0;
        endcase
    end
endmodule